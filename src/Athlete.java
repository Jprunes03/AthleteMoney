public class Athlete {
    //Fields
    int Rank;
    String Name;
    String Sport;
    String Nation;
    int Earnings;

    public Athlete(int rank, String name, String sport, String nation, int earnings) {
        this.Rank = rank;
        this.Name = name;
        this.Sport = sport;
        this.Nation = nation;
        this.Earnings = earnings;
    }
}
