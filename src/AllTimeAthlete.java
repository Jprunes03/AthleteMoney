public class AllTimeAthlete extends Athlete {
    //Fields
    int rank;
    String name;
    float Sport;
    float Nation;
    int Earnings;

    public AllTimeAthlete(int rank, String name, float sport, float nation, int earnings) {
        this.rank = rank;
        this.name = name;
        Sport = sport;
        Nation = nation;
        Earnings = earnings;
    }
}
